var_1 = int(input("Gib Zahl: "))
var_2 = int(input("Gib Zahl: "))

if var_1 == var_2:
    print("Die erste Zahl gleicht der zweiten Zahl.")
if var_1 > var_2:
    print("Die erste Zahl ist größer als die zweite Zahl.")
if var_1 >= var_2:
    print("Die erste Zahl ist größer gleich der zweiten Zahl.")
if var_1 < var_2:
    print("Die erste Zahl ist kleiner als die zweite Zahl.")
